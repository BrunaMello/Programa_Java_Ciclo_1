    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {
            
            String titulo = txtPesquisar.getText();
            
            AcervoDAO acervodao = new AcervoDAO();
            
            List<Acervo> listaacervo = acervodao.PesquisarLivroTitulo(titulo);
            
            DefaultTableModel tabelapadrao = (DefaultTableModel) tabelaLivro.getModel();
            tabelapadrao.setNumRows(0);
            
            for (Acervo livro : listaacervo) {
                tabelapadrao.addRow(new Object[] {
                    livro.getIdAcervo(),
                    livro.getTitulo(),
                    livro.getAutor(),
                    livro.getAno(),
                    livro.getPreco(),
                    livro.getQuantidade(),
                    livro.getTipo(),
                    livro.getObjeditora().getIdEditora(),
                    livro.getObjeditora().getNomeEditora(),
                });
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao pesquisar: " + e);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

