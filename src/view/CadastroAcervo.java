    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
            
            //pegar o id acervo que esta no campo de texto 
            Acervo acervo = new Acervo();
            
            //passar de string para int
            acervo.setIdAcervo(Integer.parseInt(txtId.getText()));
            
            //chamar o comando DELETE
            AcervoDAO acervodelete = new AcervoDAO();
            acervodelete.ExcluirLivro(acervo);
            
            ListaAcervo();
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir o registro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

