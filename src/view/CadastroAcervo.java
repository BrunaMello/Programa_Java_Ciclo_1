    public void ListaAcervo(){
        try {
            AcervoDAO acervo = new AcervoDAO();
            
            List<Acervo> listaacervo = acervo.ListarLivro();
            
            DefaultTableModel tabelapadrao = (DefaultTableModel) tabelaLivro.getModel();
            tabelapadrao.setNumRows(0);
            
            for (Acervo livro : listaacervo) {
                tabelapadrao.addRow(new Object[] {
                    livro.getIdAcervo(),
                    livro.getTitulo(),
                    livro.getAutor(),
                    livro.getAno(),
                    livro.getPreco(),
                    livro.getQuantidade(),
                    livro.getTipo(),
                    livro.getObjeditora().getIdEditora(),
                    livro.getObjeditora().getNomeEditora(),
                });
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "erro: " + e);
        }
    }

