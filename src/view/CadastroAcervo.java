    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            //instanciar o acervo e a editora
            Acervo acervo = new Acervo();
            Editora editora = new Editora();

            //pegando todos as strings
            acervo.setAutor(txtAutor.getText());
            acervo.setTitulo(txtTitulo.getText());

            //pegando todos os int
            acervo.setAno(Integer.parseInt(txtAno.getText()));
            acervo.setQuantidade(Integer.parseInt(txtQtd.getText()));
            acervo.setTipo(Integer.parseInt(txtTipo.getText()));

            //pegando o double
            acervo.setPreco(Double.parseDouble(txtPreco.getText()));

            //pegando dados da combo box
            editora = (Editora) cbxEditora.getSelectedItem();
            acervo.setObjeditora(editora);

            //chamando o comando INSERT
            AcervoDAO acervoinsert = new AcervoDAO();
            acervoinsert.CadastrarLivro(acervo);

            ListaAcervo();

            LimpaCampos();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

