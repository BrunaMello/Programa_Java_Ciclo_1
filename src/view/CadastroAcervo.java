    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            //pegar os dados digitados da tela e colocar em um obj
            Acervo acervo = new Acervo();
            Editora editora = new Editora();

            //pegando os que sao string
            acervo.setTitulo(txtTitulo.getText());
            acervo.setAutor(txtAutor.getText());

            //pegando os que sao int
            acervo.setIdAcervo(Integer.parseInt(txtId.getText()));
            acervo.setAno(Integer.parseInt(txtAno.getText()));
            acervo.setQuantidade(Integer.parseInt(txtQtd.getText()));
            acervo.setTipo(Integer.parseInt(txtTipo.getText()));

            //pegando os que sao double
            acervo.setPreco(Double.parseDouble(txtPreco.getText()));

            //pegar o valor da cbx no campo id
            editora = (Editora) cbxEditora.getSelectedItem();
            acervo.setObjeditora(editora);

            //chamar comando insert 
            AcervoDAO alteraracervo = new AcervoDAO();
            alteraracervo.AtualizarLivro(acervo);

            JOptionPane.showMessageDialog(null, "Registro alterado com sucesso!");
            
            ListaAcervo();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao alterar o registro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

